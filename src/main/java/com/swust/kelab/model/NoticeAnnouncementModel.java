package com.swust.kelab.model;

import java.util.Date;

import com.swust.kelab.utils.CommonUtil;

public class NoticeAnnouncementModel {

	private Integer noanId;
	private String noanTitle;
	private String noanContent;
	private String noanRolename;
	private String noanPubtime;
	private String noanTopdays;
	private String noanIsimport;
	private Integer noanCreatorid;
	private Integer userId;
	private String userName;
	private String noanRoleids;
	private Integer noanAttachment;
	
	private String deparIds;
	private String courIds;
	
	public String getDeparIds() {
		return deparIds;
	}
	public void setDeparIds(String deparIds) {
		this.deparIds = deparIds;
	}
	public String getCourIds() {
		return courIds;
	}
	public void setCourIds(String courIds) {
		this.courIds = courIds;
	}
	public Integer getNoanId() {
		return noanId;
	}
	public void setNoanId(Integer noanId) {
		this.noanId = noanId;
	}
	public String getNoanTitle() {
		return noanTitle;
	}
	public void setNoanTitle(String noanTitle) {
		this.noanTitle = noanTitle;
	}
	public String getNoanContent() {
		return noanContent;
	}
	public void setNoanContent(String noanContent) {
		this.noanContent = noanContent;
	}
	public String getNoanPubtime() {
		return noanPubtime;
	}
	public void setNoanPubtime(Date noanPubtime) {
		this.noanPubtime = CommonUtil.getFormatDateString("yyyy-MM-dd HH:mm", noanPubtime);
	}
	public String getNoanTopdays() {
		return noanTopdays;
	}
	public void setNoanTopdays(String noanTopdays) {
		this.noanTopdays = noanTopdays;
	}
	public String getNoanIsimport() {
		return noanIsimport;
	}
	public void setNoanIsimport(String noanIsimport) {
		this.noanIsimport = noanIsimport;
	}
	public Integer getUserId() {
		return userId;
	}
	public void setUserId(Integer userId) {
		this.userId = userId;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getNoanRolename() {
		return noanRolename;
	}
	public void setNoanRolename(String noanRolename) {
		this.noanRolename = noanRolename;
	}
	public String getNoanRoleids() {
		return noanRoleids;
	}
	public void setNoanRoleids(String noanRoleids) {
		this.noanRoleids = noanRoleids;
	}
	public Integer getNoanCreatorid() {
		return noanCreatorid;
	}
	public void setNoanCreatorid(Integer noanCreatorid) {
		this.noanCreatorid = noanCreatorid;
	}
	public Integer getNoanAttachment() {
		return noanAttachment;
	}
	public void setNoanAttachment(Integer noanAttachment) {
		this.noanAttachment = noanAttachment;
	}
}
